#==========================================#
#         Print string using Slicing
#==========================================#

Muzammil="I am a Developer"
print (Muzammil[0:10])


Muzammil="I am a Developer"
print (Muzammil[1:len(Muzammil)])

print(Muzammil[::])
print(Muzammil[1::])
print(Muzammil[1:10:])
print(Muzammil[1:10:2])
print(Muzammil[-10:-1:2])
print(Muzammil[10:2:-2])  #Reverse Order
print(Muzammil[::-1])     #Partial Order
print(Muzammil[:-1])
